package fundamentos;

public class InterferenciaDeTipo {
	
	public static void main(String[] args) {
		
		int numero1;//variável definida.
		
		numero1 = 4;//variável inicializada.
		
		//quando vc usa o var não é necessario tipificar a variável.
		//o var irá identificar altomaticamente o tipo da variável
		var numero2 = 2;//Quando você usa o var é necessario inicializar a variável.
		
		System.out.println(numero1);
		System.out.println(numero2);
	}
}
